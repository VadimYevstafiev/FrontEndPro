const x1 = Number(prompt('Write any number'));
const x2 = Number(prompt('Write any other number'));


const y1 = x1 + x2;
const y2 = x1 - x2;
const y3 = x1 * x2;
const y4 = x1 / x2;

alert(`${x1} + ${x2} = ${y1}\n\n
    ${x1} - ${x2} = ${y2}\n\n
    ${x1} * ${x2} = ${y3}\n\n
    ${x1} / ${x2} = ${y4}\n\n`);

